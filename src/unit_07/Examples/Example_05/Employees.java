package unit_07.Examples.Example_05;

public class Employees {
    protected long ID;
    protected long salary;

    protected void setID(long ID) {
        this.ID = ID;
    }

    public void setSalary(long salary) {
        this.salary = salary;
    }

    public long getID() {
        return ID;
    }

    public long getSalary() {
        return salary;
    }

}
