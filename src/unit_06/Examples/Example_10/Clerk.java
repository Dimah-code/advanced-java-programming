package unit_06.Examples.Example_10;

public class Clerk {
    private float hours;
    private float rate;
    private String name;

    public void setHours(float hours) {
        this.hours = hours;
    }

    public void setRate(float rate) {
        this.rate = rate;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getHours() {
        return hours;
    }

    public float getRate() {
        return rate;
    }


    public String getName() {
        return name;
    }

}
